// scripts/generate-tool-content.js
// Builds public/js/tool-content.js — a per-tool content map (benefits,
// use cases, FAQ, blog slug) derived from scripts/blog-data.js.
// tool-page.js loads this and renders the extra SEO sections only when
// content exists for the current tool.
//
// Usage: node scripts/generate-tool-content.js

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { BLOGS } from './blog-data.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname  = path.dirname(__filename);

const OUT = path.join(__dirname, '..', 'public', 'js', 'tool-content.js');

// Map tool URL slug (e.g. 'merge-pdf') → the blog entry. Most blog slugs are
// `<urlSlug>-guide` so we strip the suffix.
const BY_TOOL_SLUG = {};
for (const b of BLOGS) {
  const toolSlug = b.toolSlug.replace(/^\/+/, '');
  BY_TOOL_SLUG[toolSlug] = {
    blogSlug: b.slug,
    benefits: b.benefits.slice(0, 4),
    useCases: b.useCases.slice(0, 3),
    faq:      b.faq.slice(0, 4),
  };
}

const body = `// AUTO-GENERATED by scripts/generate-tool-content.js — do not edit.
// Per-tool SEO content (benefits, use cases, FAQ) for the upload step.
window.TOOL_CONTENT = ${JSON.stringify(BY_TOOL_SLUG, null, 2)};
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, body, 'utf8');
console.log(`✅ Wrote ${OUT} (${Object.keys(BY_TOOL_SLUG).length} tools)`);
